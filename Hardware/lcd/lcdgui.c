/******************************************************************
 * 函 数 名 称：LCD_Fill
 * 函 数 说 明：在指定区域填充颜色
 * 函 数 形 参：xsta,ysta   起始坐标
                xend,yend   终止坐标
				color       要填充的颜色
 * 函 数 返 回：无
 * 作       者： LC
 * 备       注：无
******************************************************************/
void LCD_Fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
{          
	uint16_t i,j; 
	LCD_Address_Set(xsta,ysta,xend-1,yend-1);//设置显示范围
	for(i=ysta;i<yend;i++)
	{													   	 	
		for(j=xsta;j<xend;j++)
		{
			LCD_WR_DATA(color);
		}
	} 					  	    
}

