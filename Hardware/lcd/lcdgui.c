/******************************************************************
 * 函 数 名 称：mypow
 * 函 数 说 明：计算数字
 * 函 数 形 参：m底数，n指数
 * 函 数 返 回：计算值
 * 作       者：LC
 * 备       注：无
******************************************************************/
uint32_t mypow(uint8_t m,uint8_t n)
{
	uint32_t result=1;	 
	while(n--)result*=m;
	return result;
}


